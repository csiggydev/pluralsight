# correspond to foreach loops - request items one by one from a collection

cities = ["London", "New York", "San Francisco", "Miami", "Denver", "Austin"]

for city in cities:
    print(city)

# Print values in a dictionary used in a for loop

colors = {'crimson': 0xdc143c, 'coral': 0xff7f50, 'teal': 0x008080}

for color in colors:
    print(color, colors[color])
    